export function updateUnitFields({ acres, lat, lng }) {
  setAuto('f-acres',  `${acres} acres`);
  setAuto('f-coords', `${lat.toFixed(5)}, ${lng.toFixed(5)}`);
}

export function updateLocation(locationStr) {
  setAuto('f-location', locationStr);
}

export function setLoading(bodyId, msg) {
  const el = document.getElementById(bodyId);
  if (el) el.innerHTML = `<p class="plan-loading">${msg}</p>`;
}

export function setError(bodyId, msg) {
  const el = document.getElementById(bodyId);
  if (el) el.innerHTML = `<p class="plan-error">${msg}</p>`;
}

function setAuto(id, value) {
  const el = document.getElementById(id);
  if (!el) return;
  el.textContent = value;
  el.classList.remove('pending');
}
